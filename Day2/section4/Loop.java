public class Loop{

    public static void main(String[] args) {
        for(int i = 0;i<10;i++){
            System.out.println(i);
        }

        for(int i = 1;i<11;i++){
            System.out.println("Hello");
            System.out.println(i);
        }
        System.out.println("\nMultiplication");
        for(int i = 1;i<11;i++){
            System.out.println(i*5);
        }
    }

}